    private void initTablasClientes(){
        tabCli = (DefaultTableModel) tablaClientes.getModel();
        clientes = taller.searchClientes("");
        tabCli.setNumRows(clientes.size());
                
        for(int i=0; i<clientes.size(); ++i){
            tablaClientes.setValueAt(clientes.get(i).getNombre(), i, 0);
            tablaClientes.setValueAt(clientes.get(i).getDNI(), i, 1);
            tablaClientes.setValueAt(clientes.get(i).getTlf(), i, 2);
            tablaClientes.setValueAt(clientes.get(i).getMvl(), i, 3);
            tablaClientes.setValueAt(clientes.get(i).getMail(), i, 4);
            tablaClientes.setValueAt(clientes.get(i).getDir(), i, 5);
        }
                     
    }
    
    
    private void initTablasCoches(){
        tabCch = (DefaultTableModel) tablaCoches.getModel();
        coches = taller.searchCoches("");
        tabCch.setNumRows(coches.size());
               
        for(int i=0; i<coches.size(); ++i){
            tablaCoches.setValueAt(coches.get(i).getMatricula(), i, 0);
            tablaCoches.setValueAt(coches.get(i).getMarca(), i, 1);
            tablaCoches.setValueAt(coches.get(i).getModelo(), i, 2);
            tablaCoches.setValueAt(coches.get(i).getNumKm(), i, 3);
            tablaCoches.setValueAt(coches.get(i).getNumBast(), i, 4);
            tablaCoches.setValueAt(coches.get(i).getPropietario(), i, 5);
        }
       
    }

