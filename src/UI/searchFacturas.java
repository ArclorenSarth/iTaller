    private void initTabla(int rows){
        /*tabFac=new DefaultTableModel();
        tabFac.addColumn("Fecha");
        tabFac.addColumn("Matricula");
        tabFac.setNumRows(rows);
        tablaFacturas.setModel(tabFac);*/
        tabFac = (DefaultTableModel) tablaFacturas.getModel();
        tabFac.setNumRows(rows);
        for(int i=0; i < rows; ++i){
            tablaFacturas.setValueAt(fecha(facs.get(i)), i, 0);
            tablaFacturas.setValueAt(matricula(facs.get(i)), i, 1);
        }
        
    }
    
    String matricula(String text){
        String[] dirs = text.split("/");
        String file = dirs[dirs.length-1];
        int dot = file.indexOf('.',10);
        System.out.println("mat:" + file.substring(11,dot));
        return file.substring(11,dot);
    }
    
    String fecha(String text){
        String[] dirs = text.split("/");
        String file = dirs[dirs.length-1];
        return file.substring(0,10);
    }

