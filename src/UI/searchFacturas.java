    private void tablaFacturasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaFacturasMousePressed
        //String c = (String)tablaFacturas.getValueAt(tablaFacturas.getSelectedRow(),1);
        String c = matricula(facs.get(tablaFacturas.getSelectedRow()));
        System.out.println("esa:" + matricula(facs.get(tablaFacturas.getSelectedRow())));
        System.out.println("sas:" + c);
        Coche ch;
        Cliente cl;
        if(taller.searchCoches(c).size() > 0) ch = taller.searchCoches(c).get(0);
        else ch = new Coche("-NA-","-NA-","-NA-","-NA-","-NA-","-NA-");
        if(taller.searchClientes(ch.getPropietario()).size() > 0)
        cl = taller.searchClientes(ch.getPropietario()).get(0);
        else cl = new Cliente("-NA-","-NA-","-NA-","-NA-","-NA-","-NA-","-NA-");

        marcaText.setText(ch.getMarca());
        modeloText.setText(ch.getModelo());
        kmText.setText(ch.getNumKm());
        bastText.setText(ch.getNumBast());

        nombreText.setText(cl.getNombre());
        dniText.setText(cl.getDNI());
        dirText.setText(cl.getDir());
        mailText.setText(cl.getMail());
        tlfText.setText(cl.getTlf());
        mvlText.setText(cl.getMvl());
        obsText.setText(cl.getObs());

        ArrayList<String> cochesCl = cl.getCoches();
        cochesModel.clear();
        for(int i = 0; i < cochesCl.size(); ++i){
            cochesModel.addElement(cochesCl.get(i));
        }
        cochesList.setModel(cochesModel);
    }//GEN-LAST:event_tablaFacturasMousePressed

